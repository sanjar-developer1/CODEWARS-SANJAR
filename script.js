// Math.round()	Yaqinlashtiradi	Math.round(4.6) → 5
// Math.floor()	Pastga qarab butun qiladi	Math.floor(4.9) → 4
// Math.ceil()	Yuqoriga qarab butun qiladi	Math.ceil(4.1) → 5
// Math.random()	0–1 oralig‘ida tasodifiy son	Math.random()
// Math.pow(a,b)	Darajaga ko‘taradi	Math.pow(2,3) → 8
// Math.sqrt()	Ildiz ostini oladi	Math.sqrt(25) → 5

















