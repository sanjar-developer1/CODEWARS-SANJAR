// Array Metod	Vazifasi	Misol
// push()	Oxiriga qo‘shadi	arr.push(5)
// pop()	Oxiridan o‘chiradi	arr.pop()
// shift()	Boshidan o‘chiradi	arr.shift()
// unshift()	Boshiga qo‘shadi	arr.unshift(10)
// slice(a, b)	a dan b gacha kesadi	arr.slice(1, 3)
// splice(a, b)	a dan boshlab b ta o‘chiradi	arr.splice(1, 2)
// concat()	Arraylarni qo‘shadi	arr1.concat(arr2)
// join()	Arrayni string qiladi	arr.join(", ")
// includes()	Element bor-yo‘qligini tekshiradi	arr.includes(5)
// indexOf()	Elementning indexini qaytaradi	arr.indexOf(3)
// reverse()	Teskari qiladi	arr.reverse()
// sort()	Saralaydi

// string Metod	Vazifasi	Misol
// length	Uzunligini beradi	"salom".length → 5
// toUpperCase()	Katta harf	"salom".toUpperCase()
// toLowerCase()	Kichik harf	"SALOM".toLowerCase()
// trim()	Bo‘sh joylarni olib tashlaydi	" salom ".trim()
// includes()	Ichida bormi tekshiradi	"salom".includes("lo")
// indexOf()	Belgining indexi	"salom".indexOf("l")
// slice(a,b)	a dan b gacha kesadi	"salom".slice(1,3)
// substring(a,b)	slicega o‘xshaydi	"salom".substring(1,4)
// replace()	So‘zni almashtiradi	"olma".replace("o","q")
// split()	Stringni arrayga aylantiradi	"salom dunyo".split(" ")

//Math Metod	Vazifasi	Misol
